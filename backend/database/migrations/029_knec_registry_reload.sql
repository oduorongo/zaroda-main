-- ============================================================================
-- 029_knec_registry_reload.sql
-- Re-load the KNEC school registry. Migration 022 may have been recorded as
-- applied on some databases without its data actually loading (the registry
-- showed "0 codes available for signup lookup"). This migration re-runs the
-- bulk insert idempotently (ON CONFLICT DO NOTHING) under a NEW filename so it
-- executes once on the next boot and fills any empty registry.
-- ============================================================================

CREATE TABLE IF NOT EXISTS knec_school_registry (
  id           UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  knec_code    VARCHAR(20) NOT NULL,
  name         VARCHAR(200),
  level        VARCHAR(40),
  county       VARCHAR(80),
  sub_county   VARCHAR(80),
  zone         VARCHAR(80),
  category     VARCHAR(60),
  created_at   TIMESTAMPTZ DEFAULT NOW()
);
ALTER TABLE knec_school_registry ALTER COLUMN name DROP NOT NULL;

-- The table may already exist WITHOUT a unique constraint on knec_code (older
-- create), which makes ON CONFLICT (knec_code) fail. Remove any duplicate codes,
-- then add the unique constraint if it isn't there, so the inserts below can use it.
DELETE FROM knec_school_registry a
  USING knec_school_registry b
 WHERE a.ctid < b.ctid AND a.knec_code = b.knec_code;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'knec_school_registry_knec_code_key'
  ) AND NOT EXISTS (
    SELECT 1 FROM pg_indexes
     WHERE tablename = 'knec_school_registry' AND indexdef ILIKE '%UNIQUE%knec_code%'
  ) THEN
    BEGIN
      ALTER TABLE knec_school_registry ADD CONSTRAINT knec_school_registry_knec_code_key UNIQUE (knec_code);
    EXCEPTION WHEN duplicate_table OR duplicate_object THEN
      -- constraint already present under another name; ignore
      NULL;
    END;
  END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_knec_registry_code ON knec_school_registry(knec_code);


